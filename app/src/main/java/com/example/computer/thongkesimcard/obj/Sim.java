package com.example.computer.thongkesimcard.obj;

public class Sim {
    private String name, dateNap, dateMua;
    private int moneyNap, moneyMua;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getDateNap() {
        return dateNap;
    }

    public void setDateNap(String dateNap) {
        this.dateNap = dateNap;
    }

    public String getDateMua() {
        return dateMua;
    }

    public void setDateMua(String dateMua) {
        this.dateMua = dateMua;
    }

    public int getMoneyNap() {
        return moneyNap;
    }

    public void setMoneyNap(int moneyNap) {
        this.moneyNap = moneyNap;
    }

    public int getMoneyMua() {
        return moneyMua;
    }

    public void setMoneyMua(int moneyMua) {
        this.moneyMua = moneyMua;
    }
}
